// Operadores

let a = 3, b = 2;
let z = a + b;
console.log('z :>> ', z);

z = a - b;
console.log('z :>> ', z);

z = a * b;
console.log('z :>> ', z);

z = a / b;
console.log('z :>> ', z);

z = a % b; // modulo
console.log('z :>> ', z);

z = a ** b; // exponente
console.log('z :>> ', z);

z = ++a; //pre incremento
console.log("z :>> ", z);

z = b++; // post incremento
console.log("b :>> ", b);
console.log("z :>> ", z);

z = --a;
console.log("a :>> ", a);
console.log("z :>> ", z);

z = a--;
console.log("a :>> ", a);
console.log("z :>> ", z);
